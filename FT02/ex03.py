'''Escreve um programa em python que solicite três números inteiros ao utilizador e imprima a soma dos mesmos.'''
# Solicitar três números inteiros ao utilizador
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))
num3 = int(input("Digite o terceiro número: "))
# Calcular a soma dos três números
soma = num1 + num2 + num3
# Imprimir a soma
print("A soma dos três números é:", soma)
